import numpy as np
import pandas as pd
import warnings
warnings.filterwarnings('ignore')
import matplotlib.pyplot as plt
import seaborn as sns

df = pd.read_csv('health.data.cleaned.csv')
df.head()

for col in df.select_dtypes(exclude = 'object'):
    sns.boxplot(y = df[col], color = 'grey')
    plt.show()

for col in df.select_dtypes(exclude = 'object'):
    sns.histplot(x = df[col], bins = 50, kde = True, color = 'skyblue')
    plt.show()

df['Income'].value_counts().plot(kind = 'pie', autopct = '%.1f')
plt.ylabel(None)
plt.show()

for col in df.select_dtypes(include = 'object'):
    sns.countplot(x = df[col], palette = 'vlag')
    plt.show()

plt.figure(figsize=(12,6))
sns.heatmap(df.select_dtypes(exclude= ['object']).corr(), annot = True, cmap = 'viridis', fmt = '.1f')